    def diyet_egzersiz_oneri(self, kan_sekeri, belirtiler):
        """
        Kan ≈üekeri seviyesi ve belirtilere g√∂re diyet ve egzersiz √∂nerisi verir.
        Tablodaki kurallara g√∂re otomatik √∂neri √ºretir.
        
        Args:
            kan_sekeri: int, hastanƒ±n kan ≈üekeri seviyesi (mg/dL)
            belirtiler: list of str, kaydedilmi≈ü hasta belirtileri
            
        Returns:
            tuple: (diyet_onerisi, egzersiz_onerisi)
        """
        # Belirtiler listesini k√º√ß√ºk harfe d√∂n√º≈üt√ºr
        belirtiler_lower = [b.lower() if b else "" for b in belirtiler]
        
        # < 70 mg/dL (Hipoglisemi)
        if kan_sekeri < 70:
            if any(belirti in b.lower() for b in belirtiler_lower for belirti in ["n√∂ropati", "polifaj", "yorgunluk"]):
                return "Dengeli Beslenme", "Yok"
            return "Dengeli Beslenme", "Yok"  # Varsayƒ±lan hipoglisemi √∂nerisi
            
        # 70-110 mg/dL (Normal - Alt D√ºzey)
        elif 70 <= kan_sekeri <= 110:
            if any(belirti in b.lower() for b in belirtiler_lower for belirti in ["yorgun", "kilo kayb"]):
                return "Az ≈ûekerli Diyet", "Y√ºr√ºy√º≈ü"
            elif any(belirti in b.lower() for b in belirtiler_lower for belirti in ["polifaj", "polidipsi"]):
                return "Dengeli Beslenme", "Y√ºr√ºy√º≈ü"
            return "Dengeli Beslenme", "Y√ºr√ºy√º≈ü"  # Varsayƒ±lan normal alt d√ºzey √∂nerisi
            
        # 110-180 mg/dL (Normal - √úst D√ºzey / Hafif Y√ºksek)
        elif 110 < kan_sekeri < 180:
            if any(belirti in b.lower() for b in belirtiler_lower for belirti in ["bulanƒ±k g√∂rme", "n√∂ropati"]):
                return "Az ≈ûekerli Diyet", "Klinik Egzersiz"
            elif any(belirti in b.lower() for b in belirtiler_lower for belirti in ["poli√ºri", "polidipsi"]):
                return "≈ûekersiz Diyet", "Klinik Egzersiz"
            elif any(belirti in b.lower() for b in belirtiler_lower for belirti in ["yorgun", "n√∂ropati", "bulanƒ±k"]):
                return "Az ≈ûekerli Diyet", "Y√ºr√ºy√º≈ü"
