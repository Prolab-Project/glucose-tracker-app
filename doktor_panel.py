    def hastalari_getir(self):
        self.hasta_listesi.clear()  
        
        # Filtreleri al
        kan_sekeri_filtre = self.kan_sekeri_filtre.currentText()
        belirti_filtre = self.belirti_filtre.currentText()
        tarih_filtre = self.tarih_filtre.currentText()
        
        # T√ºm hastalarƒ± al
        hastalar = self.db.get_doctor_patients(self.doktor['id'])
        filtrelenmis_hastalar = []
        
        for hasta in hastalar:
            hasta_id = hasta[0]
            eklenecek = True
            
            # Kan ≈üekeri filtresini uygula
            if kan_sekeri_filtre != "Hepsi":
                olcumler = self.db.get_patient_measurements(hasta_id)
                if olcumler:
                    # Son √∂l√ß√ºm yerine t√ºm √∂l√ß√ºmlerin ortalamasƒ±nƒ± hesapla
                    toplam_kan_sekeri = 0
                    olcum_sayisi = 0
                    
                    for olcum in olcumler:
                        toplam_kan_sekeri += olcum[4]  # olcum[4] = kan_seker_degeri
                        olcum_sayisi += 1
                    
                    # Ortalama kan ≈üekeri deƒüeri
                    ortalama_kan_sekeri = toplam_kan_sekeri / olcum_sayisi if olcum_sayisi > 0 else 0
                    
                    # Ortalama deƒüere g√∂re filtreleme yap
                    if kan_sekeri_filtre == "D√º≈ü√ºk (<70 mg/dL)" and ortalama_kan_sekeri >= 70:
                        eklenecek = False
                    elif kan_sekeri_filtre == "Normal (70-110 mg/dL)" and (ortalama_kan_sekeri < 70 or ortalama_kan_sekeri > 110):
                        eklenecek = False
                    elif kan_sekeri_filtre == "Y√ºksek Normal (111-150 mg/dL)" and (ortalama_kan_sekeri < 111 or ortalama_kan_sekeri > 150):
                        eklenecek = False
                    elif kan_sekeri_filtre == "Y√ºksek (151-200 mg/dL)" and (ortalama_kan_sekeri < 151 or ortalama_kan_sekeri > 200):
                        eklenecek = False
                    elif kan_sekeri_filtre == "√áok Y√ºksek (>200 mg/dL)" and ortalama_kan_sekeri <= 200:
                        eklenecek = False
                else:
                    # √ñl√ß√ºm yoksa ve filtre uygulanƒ±yorsa, hastayƒ± listeye alma
                    if kan_sekeri_filtre != "Hepsi":
                        eklenecek = False
            
            # Belirti filtresini uygula
            if eklenecek and belirti_filtre != "Hepsi":
                belirtiler = self.db.get_patient_symptoms(hasta_id)
                belirti_var = False
                
                for belirti in belirtiler:
                    if belirti[3] == belirti_filtre:
                        belirti_var = True
                        break
                
                if not belirti_var:
                    eklenecek = False
            
            # Tarih filtresini uygula
            if eklenecek and tarih_filtre != "Hepsi":
                olcumler = self.db.get_patient_measurements(hasta_id)
                tarih_filtresine_uyuyor = False
                
                if olcumler:
                    bugun = datetime.now().date()
                    
                    for olcum in olcumler:
                        olcum_tarihi = olcum[3].date()
                        fark = (bugun - olcum_tarihi).days
                        
                        if tarih_filtre == "Bug√ºn" and fark == 0:
                            tarih_filtresine_uyuyor = True
                            break
                        elif tarih_filtre == "Son 3 G√ºn" and fark <= 2:
                            tarih_filtresine_uyuyor = True
                            break
                        elif tarih_filtre == "Son 7 G√ºn" and fark <= 6:
                            tarih_filtresine_uyuyor = True
                            break
                        elif tarih_filtre == "Son 30 G√ºn" and fark <= 29:
                            tarih_filtresine_uyuyor = True
                            break
                
                if not tarih_filtresine_uyuyor:
                    eklenecek = False
            
            if eklenecek:
                filtrelenmis_hastalar.append(hasta)
        
        # Filtrelenmi≈ü hastalarƒ± listele
        for hasta in filtrelenmis_hastalar:
            self.hasta_listesi.addItem(f"{hasta[2]} {hasta[3]} - TC: {hasta[1]}")
        
        # Filtre sonu√ßlarƒ±nƒ± g√∂ster
        if not filtrelenmis_hastalar:
            self.hasta_listesi.addItem("Filtrelere uygun hasta bulunamadƒ±.")
    
