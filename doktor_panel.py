            for olcum in olcumler:
                tarih = olcum[3]  # √∂l√ß√ºm tarihi
                deger = olcum[4]  # kan_seker_degeri
                zaman = olcum[5] if olcum[5] else "Belirtilmemi≈ü"  # √∂l√ß√ºm_zamani
                
